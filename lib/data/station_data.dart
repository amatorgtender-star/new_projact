/// 앱 시작 시 호출하여 API로부터 최신 역 목록을 받아오는 함수
Future<void> initializeStations() async {
  try {
    final fetched = await SubwayApiService.fetchAllStations();
    if (fetched.isNotEmpty) {
      // 기존 역 목록과 합치되, API에서 가져온 정보로 업데이트
      final Map<String, SubwayStation> stationMap = {};

      // 1. 기존 정적 리스트 먼저 담기
      for (var s in stations) {
        stationMap['${s.stationName}|${s.lineName}'] = s;
      }

      // 2. API 결과를 덮어씌우기 (API 데이터가 더 정확할 가능성 높음)
      for (var s in fetched) {
        stationMap['${s.stationName}|${s.lineName}'] = s;
      }

      stations = stationMap.values.toList();
      stations.sort((a, b) => a.stationName.compareTo(b.stationName));

      debugPrint('역 목록 업데이트 완료: 총 ${stations.length}개');
    } else {
      debugPrint('역 목록 API 응답 없음 — 기본 리스트 사용 (${stations.length}개)');
    }
  } catch (e) {
    debugPrint('역 목록 로드 실패: $e — 기본 리스트 사용 (${stations.length}개)');
  }
}
